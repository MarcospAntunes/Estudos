const nomes = ["João", "Ana", "Caio", "Lara", "Marjorie", "Leo"];

nomes.splice(1, 2, "Rodrigo"); // -> Ele altera diretamente o Array!!

// O metodo splice aceita 3 argumentos: o primeiro é o indice do item que você quer remover, o segundo é quantos elementos você quer remover a partir do indice indicado e o terceiro metódo serve para adicionar um novo item NO LUGAR do item removido da lista, no caso será no inidice 1.

